static void test_suit_storage_update_set(void)
{
	void *addresses[] = {NULL, (void*)0xCAFEFECA};
	size_t sizes[] = {0, SUIT_STORAGE_SIZE + 1, SUIT_STORAGE_SIZE, 0x2048, 2044};
	int rc = 0;

	for (int addr_i = 0; addr_i < ARRAY_SIZE(addresses); addr_i++) {
		for (int size_i = 0; size_i < ARRAY_SIZE(sizes); size_i++) {
			void *addr = addresses[addr_i];
			size_t size = sizes[size_i];

			if ((addr != NULL) && (size > 0)) {
				rc = suit_storage_update_set(addr, size);
				zassert_equal(rc, 0,
					"Setting correct DFU partition failed (0x%x, %d).",
					addr, size);

				/* Verify set values */
				bool available = suit_storage_update_available();
				zassert_equal(available, true,
					"Set succeeded, but update availability is not reported.");

				void *read_addr;
				size_t read_size;
				rc = suit_storage_update_get(&read_addr, &read_size);
				zassert_equal(read_addr, addr,
					"Update set with wrong address (%p != %p)",
					read_addr, addr);
				zassert_equal(read_size, size,
					"Update set with wrong size (0x%x != 0x%x)",
					read_size, size);
			} else {
				rc = suit_storage_update_set(addr, size);
				zassert_not_equal(rc, 0,
					"Setting incorrect DFU partition should fail (0x%x, %d).",
					addr, size);

				bool available = suit_storage_update_available();
				zassert_equal(available, false,
					"Set succeeded, but update availability is reported.");
			}
		}
	}
}


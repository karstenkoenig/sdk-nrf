static void test_setup_erased(void)
{
	uint32_t * storage = (uint32_t *)SUIT_STORAGE_ADDRESS;

	if (*storage == UPDATE_MAGIC_VALUE_EMPTY) {
		return;
	}

	/* Erase the area, to met the preconditions in the next test. */
	const struct device *fdev = DEVICE_DT_GET(DT_CHOSEN(zephyr_flash_controller));
	zassert_not_null(fdev, "Unable to find a driver to erase area");

	int rc = flash_erase(fdev, SUIT_STORAGE_OFFSET, SUIT_STORAGE_SIZE);
	zassert_equal(rc, 0, "Unable to erase memory before test execution");
}

static bool has_preconfigured_update(void)
{
	void * addr;
	size_t size;

	return ((suit_storage_update_get(&addr, &size) == 0) &&
	    (addr == (void *)0xCAFEFECA) &&
	    (size == 2044));
}

static void test_setup_valid_update(void)
{
	if (has_preconfigured_update()) {
		return;
	}

	/* Erase the area, to met the preconditions in the next test. */
	const struct device *fdev = DEVICE_DT_GET(DT_CHOSEN(zephyr_flash_controller));
	zassert_not_null(fdev, "Unable to find a driver to erase area");

	int rc = flash_erase(fdev, SUIT_STORAGE_OFFSET, SUIT_STORAGE_SIZE);
	zassert_equal(rc, 0, "Unable to erase memory before test execution");

	rc = suit_storage_update_set((void*)0xCAFEFECA, 2044);
	zassert_equal(rc, 0, "Unable to set correct DFU before test execution (0x%x, %d).",
		0xCAFEFECA, 2044);
}

static void test_suit_storage_init(void)
{
	size_t size;
	void *addr;
	int rc = 0;

	rc = suit_storage_install_envelope(0, manifest_buf, manifest_len);
	zassert_not_equal(rc, 0, "Envelope installed on uninitialized SUIT storage.");

	rc = suit_storage_installed_envelope_get(0, &addr, &size);
	zassert_not_equal(rc, 0, "Envelope received from uninitialized SUIT storage.");

	rc = suit_storage_update_clear();
	zassert_not_equal(rc, 0, "Update info cleared in uninitialized SUIT storage.");

	rc = suit_storage_update_set((void *)0xCAFECAFE, 1024);
	zassert_not_equal(rc, 0, "Update info set in uninitialized SUIT storage.");

	bool available = suit_storage_update_available();
	zassert_not_equal(available, true, "Update available on uninitialized SUIT storage.");

	rc = suit_storage_update_get(&addr, &size);
	zassert_not_equal(rc, 0, "Update info returned from uninitialized SUIT storage.");

	rc = suit_storage_init();
	zassert_equal(rc, 0, "Failed to initialize SUIT storage module (%d).", rc);
}


static void test_suit_storage_empty_envelopes(void)
{
	void *addr;
	size_t size;
	int rc = 0;

	for (size_t i = 0; i < CONFIG_SUIT_PARTITION_N_ENVELOPES; i++) {
		rc = suit_storage_installed_envelope_get(i, NULL, NULL);
		zassert_not_equal(rc, 0,
			"Arguments are incorrect, but %d envelope was received", i);

		rc = suit_storage_installed_envelope_get(i, NULL, &size);
		zassert_not_equal(rc, 0,
			"Arguments are incorrect, but %d envelope was received (%d)",
			i, size);

		rc = suit_storage_installed_envelope_get(i, &addr, NULL);
		zassert_not_equal(rc, 0,
			"Arguments are incorrect, but %d envelope was received (0x%x)",
			i, addr);

		rc = suit_storage_installed_envelope_get(i, &addr, &size);
		zassert_not_equal(rc, 0,
			"Suit storage is empty, but %d envelope was received (0x%x, %d)",
			i, addr, size);
	}
}

